package chapter02;

public class CastingOperator {

	public static void main(String[] args) {
		
		char c = 'a';
		System.out.println(c); // a (97)
		
		int n1 = 1;
		System.out.println(c + n1); // 98

		int n2 = 10;
		System.out.println(n2); // 10
		
		float n3 = 10.0f;
		System.out.println(n2 + n3); // 20.0
	}

}
